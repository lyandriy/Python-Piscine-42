def Morse_code(txt):
    for i in range(len(txt)):
        x = code.get(txt[i])
        if not x:
            print("ERROR")
            return None
    for i in range(len(txt)):
        print(f"{code.get(txt[i])} ", end="")
    print("\n", end="")
            

if __name__ == "__main__":
    if len(sys.argv) > 1:
        txt = ""
        for i in range(1, len(sys.argv)):
            txt = txt + sys.argv[i] + " "
        txt = txt[:-1]
        Morse_code(txt)

    